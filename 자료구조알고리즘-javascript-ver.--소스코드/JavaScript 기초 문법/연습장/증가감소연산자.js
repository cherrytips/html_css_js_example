let num, result;

num = 10;
result = num++;
console.log(result); // 10
console.log(num); // 11

num = 10;
result = ++num;
console.log(result); // 11
console.log(num); // 11

num = 10;
result = num--;
console.log(result); // 10
console.log(num); // 9

num = 10;
result = --num;
console.log(result); // 9
console.log(num); // 9
